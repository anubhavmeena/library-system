package com.library.membership.repository;

import com.library.membership.entity.Plan;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.orm.jpa.DataJpaTest;

import java.math.BigDecimal;
import java.util.List;

import static org.assertj.core.api.Assertions.*;

@DataJpaTest
class PlanRepositoryTest {

    @Autowired PlanRepository planRepository;

    @BeforeEach
    void setUp() {
        planRepository.deleteAll();
    }

    private Plan buildPlan(String name, boolean active) {
        return Plan.builder()
                .name(name)
                .planType(Plan.PlanType.FULL_DAY)
                .price(BigDecimal.valueOf(600))
                .durationDays(30)
                .isActive(active)
                .build();
    }

    @Test
    void findByIsActiveTrue_returnsOnlyActivePlans() {
        planRepository.save(buildPlan("Active Plan", true));
        planRepository.save(buildPlan("Inactive Plan", false));

        List<Plan> result = planRepository.findByIsActiveTrue();

        assertThat(result).hasSize(1)
                .first().extracting(Plan::getName).isEqualTo("Active Plan");
    }

    @Test
    void findByIsActiveTrue_excludesInactivePlans() {
        planRepository.save(buildPlan("Inactive", false));

        assertThat(planRepository.findByIsActiveTrue()).isEmpty();
    }

    @Test
    void findByIsActiveTrue_emptyTable_returnsEmptyList() {
        assertThat(planRepository.findByIsActiveTrue()).isEmpty();
    }

    @Test
    void findByIsActiveTrue_multiplePlans_returnsAll() {
        planRepository.save(buildPlan("Plan A", true));
        planRepository.save(buildPlan("Plan B", true));
        planRepository.save(buildPlan("Plan C", false));

        assertThat(planRepository.findByIsActiveTrue()).hasSize(2);
    }

    @Test
    void save_uuidAutoGenerated() {
        Plan saved = planRepository.save(buildPlan("Test Plan", true));
        assertThat(saved.getId()).isNotNull();
    }
}
