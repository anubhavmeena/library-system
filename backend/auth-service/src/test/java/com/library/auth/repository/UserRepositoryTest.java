package com.library.auth.repository;

import com.library.auth.entity.User;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.orm.jpa.DataJpaTest;
import org.springframework.dao.DataIntegrityViolationException;

import java.util.Optional;

import static org.assertj.core.api.Assertions.*;

@DataJpaTest
class UserRepositoryTest {

    @Autowired
    UserRepository userRepository;

    private User studentByMobile;
    private User studentByEmail;
    private User adminUser;

    @BeforeEach
    void setUp() {
        userRepository.deleteAll();

        studentByMobile = userRepository.save(User.builder()
                .name("Mobile Student")
                .mobile("9876543210")
                .role(User.Role.STUDENT)
                .isActive(true)
                .build());

        studentByEmail = userRepository.save(User.builder()
                .name("Email Student")
                .email("email@example.com")
                .role(User.Role.STUDENT)
                .isActive(true)
                .build());

        adminUser = userRepository.save(User.builder()
                .name("Admin User")
                .email("admin@example.com")
                .mobile("9000000000")
                .role(User.Role.ADMIN)
                .isActive(true)
                .build());
    }

    // ── existsByMobileOrEmail ─────────────────────────────────────────────────

    @Test
    void existsByMobileOrEmail_matchesByMobile() {
        assertThat(userRepository.existsByMobileOrEmail("9876543210", "no-match"))
                .isTrue();
    }

    @Test
    void existsByMobileOrEmail_matchesByEmail() {
        assertThat(userRepository.existsByMobileOrEmail("no-match", "email@example.com"))
                .isTrue();
    }

    @Test
    void existsByMobileOrEmail_noMatch_returnsFalse() {
        assertThat(userRepository.existsByMobileOrEmail("0000000000", "no@match.com"))
                .isFalse();
    }

    @Test
    void existsByMobileOrEmail_bothParamsSameContact_works() {
        assertThat(userRepository.existsByMobileOrEmail("9876543210", "9876543210"))
                .isTrue();
    }

    // ── findByMobileOrEmail ───────────────────────────────────────────────────

    @Test
    void findByMobileOrEmail_foundByMobile() {
        Optional<User> result = userRepository.findByMobileOrEmail("9876543210", "9876543210");
        assertThat(result).isPresent();
        assertThat(result.get().getName()).isEqualTo("Mobile Student");
    }

    @Test
    void findByMobileOrEmail_foundByEmail() {
        Optional<User> result = userRepository.findByMobileOrEmail("email@example.com", "email@example.com");
        assertThat(result).isPresent();
        assertThat(result.get().getName()).isEqualTo("Email Student");
    }

    @Test
    void findByMobileOrEmail_notFound_returnsEmpty() {
        Optional<User> result = userRepository.findByMobileOrEmail("0000000000", "nope@nope.com");
        assertThat(result).isEmpty();
    }

    // ── findByMobile ──────────────────────────────────────────────────────────

    @Test
    void findByMobile_found() {
        Optional<User> result = userRepository.findByMobile("9876543210");
        assertThat(result).isPresent()
                .get().extracting(User::getName).isEqualTo("Mobile Student");
    }

    @Test
    void findByMobile_notFound_returnsEmpty() {
        assertThat(userRepository.findByMobile("0000000000")).isEmpty();
    }

    // ── findByEmail ───────────────────────────────────────────────────────────

    @Test
    void findByEmail_found() {
        Optional<User> result = userRepository.findByEmail("email@example.com");
        assertThat(result).isPresent()
                .get().extracting(User::getName).isEqualTo("Email Student");
    }

    @Test
    void findByEmail_notFound_returnsEmpty() {
        assertThat(userRepository.findByEmail("nope@nope.com")).isEmpty();
    }

    // ── UUID auto-generation & lifecycle ──────────────────────────────────────

    @Test
    void save_uuidAutoGenerated() {
        assertThat(studentByMobile.getId()).isNotNull();
        assertThat(studentByEmail.getId()).isNotNull();
        assertThat(studentByMobile.getId()).isNotEqualTo(studentByEmail.getId());
    }

    @Test
    void save_createdAtAndUpdatedAtSetByPrePersist() {
        assertThat(studentByMobile.getCreatedAt()).isNotNull();
        assertThat(studentByMobile.getUpdatedAt()).isNotNull();
    }

    // ── Unique constraints ────────────────────────────────────────────────────

    @Test
    void save_duplicateMobile_throwsDataIntegrityViolation() {
        User duplicate = User.builder()
                .name("Duplicate")
                .mobile("9876543210") // same as studentByMobile
                .role(User.Role.STUDENT)
                .isActive(true)
                .build();

        assertThatThrownBy(() -> userRepository.saveAndFlush(duplicate))
                .isInstanceOf(DataIntegrityViolationException.class);
    }

    @Test
    void save_duplicateEmail_throwsDataIntegrityViolation() {
        User duplicate = User.builder()
                .name("Duplicate")
                .email("email@example.com") // same as studentByEmail
                .role(User.Role.STUDENT)
                .isActive(true)
                .build();

        assertThatThrownBy(() -> userRepository.saveAndFlush(duplicate))
                .isInstanceOf(DataIntegrityViolationException.class);
    }
}
